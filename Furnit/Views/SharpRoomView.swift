// MARK: - WebGL Boundary Manager

/// Boundary manager for WebGL Gaussian splat rendering
/// Calculates optimal camera positions based on room dimensions
struct WebGLBoundaryManager {
    let width: Float
    let height: Float
    let depth: Float

    /// Default room dimensions when bounds not provided
    static let defaultBounds = WebGLBoundaryManager(width: 4.0, height: 3.0, depth: 2.0)

    init(width: Float, height: Float, depth: Float) {
        self.width = width
        self.height = height
        self.depth = depth
    }

    init(bounds: SIMD3<Float>?) {
        if let b = bounds {
            self.width = b.x
            self.height = b.y
            self.depth = b.z
        } else {
            self = Self.defaultBounds
        }
    }

    /// Calculate optimal camera position for viewing the room
    /// Camera positioned outside room, looking at room center
    /// Returns (eye position, target position) for lookAt camera
    func getOptimalCameraPosition() -> (eye: SIMD3<Float>, target: SIMD3<Float>) {
        // Classic PLY transform: (x, -y, -z)
        // Room ends up entirely in NEGATIVE Z (front wall around -1, back wall around -depth-1)
        // Room is roughly centered in X and Y around origin
        // Estimate room center Z based on typical SHARP output pattern

        let roomCenterZ = -(depth / 2 + 1.5)  // Room center (estimated from logs)

        let eye = SIMD3<Float>(
            0,                          // Center X
            0,                          // Center Y (room is symmetric)
            6.0                         // Further back (positive Z, outside room)
        )

        let target = SIMD3<Float>(
            0,                          // Center X
            0,                          // Center Y
            roomCenterZ                 // Room center Z
        )

        return (eye: eye, target: target)
    }

    /// Get room center point
    var roomCenter: SIMD3<Float> {
        SIMD3<Float>(0, 0, 0)  // Classic PLY is centered at origin
    }

    /// Get room dimensions
    var dimensions: SIMD3<Float> {
        SIMD3<Float>(width, height, depth)
    }
}

/// Simple 3D Gaussian Splat viewer using WebGL (antimatter15/splat)
/// Renders PLY files generated by SHARP
struct SharpRoomView: View {
    let plyURL: URL
    let roomMeasurements: RoomMeasurements?  // Room measurements for display
    let allowSave: Bool  // Show save button (true for new rooms, false for viewing from home)
    @Environment(\.dismiss) private var dismiss

    // Convenience initializer for backwards compatibility
    init(plyURL: URL, roomMeasurements: RoomMeasurements? = nil, allowSave: Bool = true) {
        self.plyURL = plyURL
        self.roomMeasurements = roomMeasurements
        self.allowSave = allowSave
    }

    @State private var isLoading = true
    @State private var error: String?
    @State private var showingSmartyPants = false
    @State private var capturedImage: UIImage? = nil
    @State private var roomSnapshot: UIImage? = nil
    @State private var mlModel: MLModel? = nil

    // Save room state
    @StateObject private var modelManager = USDZModelManager()
    @State private var isSavingRoom = false
    @State private var saveProgress: Double = 0.0
    @State private var savingTimer: Timer?
    @State private var showSaveAlert = false
    @State private var saveAlertMessage = ""
    @State private var saveWasSuccessful = false
    @State private var showRoomNameInput = false
    @State private var roomName = ""

    /// Compute classic PLY URL (pre-rotated for antimatter15/splat)
    private var classicPlyURL: URL {
        let path = plyURL.path
        let classicPath = path.replacingOccurrences(of: ".ply", with: "_classic.ply")
        let classicURL = URL(fileURLWithPath: classicPath)
        if FileManager.default.fileExists(atPath: classicPath) {
            return classicURL
        }
        return plyURL
    }

    var body: some View {
        ZStack {
            // WebGL view using antimatter15/splat (with pre-rotated classic PLY)
            AntimatterSplatView(plyURL: classicPlyURL, roomBounds: roomMeasurements?.boundingBox, onLoaded: {
                isLoading = false
            })
            .ignoresSafeArea()

            // Loading overlay
            if isLoading {
                VStack(spacing: 16) {
                    ProgressView()
                        .scaleEffect(1.5)
                        .tint(.white)

                    Text("Loading 3D room...")
                        .font(.subheadline)
                        .foregroundColor(.white)
                }
                .padding(24)
                .background(Color.black.opacity(0.7))
                .cornerRadius(12)
            }

            // Error overlay
            if let error = error {
                VStack(spacing: 16) {
                    Image(systemName: "exclamationmark.triangle")
                        .font(.largeTitle)
                        .foregroundColor(.orange)
                    Text(error)
                        .foregroundColor(.white)
                        .multilineTextAlignment(.center)
                }
                .padding(24)
                .background(Color.black.opacity(0.7))
                .cornerRadius(12)
            }

            // SmartyPants overlay (when active) - full screen
            if showingSmartyPants {
                SmartyPantsUIView(
                    capturedImage: $capturedImage,
                    roomImage: roomSnapshot,
                    mlModel: mlModel,
                    processInterval: 0.07,
                    active: true
                )
                .ignoresSafeArea()
                .zIndex(100)
            }


            // Save progress overlay
            if isSavingRoom {
                saveRoomProgressOverlay
            }

            // WebGL Joystick overlay (hide when SmartyPants active)
            if !showingSmartyPants {
                WebGLJoystickOverlay()
                    .zIndex(99997)
            }

            // Bottom buttons (brain left, screenshot right)
            VStack {
                Spacer()
                HStack {
                    // Brain button (bottom-left)
                    Button(action: {
                        if showingSmartyPants {
                            showingSmartyPants = false
                        } else {
                            // Request Metal view snapshot
                            NotificationCenter.default.post(name: NSNotification.Name("CaptureMetalSnapshot"), object: nil)
                            // Wait for snapshot then show SmartyPants
                            DispatchQueue.main.asyncAfter(deadline: .now() + 0.3) {
                                showingSmartyPants = true
                            }
                        }
                    }) {
                        Image(systemName: "brain.head.profile")
                            .font(.system(size: 28))
                            .foregroundColor(.white)
                            .frame(width: 60, height: 60)
                            .background(Circle().fill(showingSmartyPants ? Color.green : Color.blue).shadow(radius: 5))
                    }
                    .disabled(isLoading)
                    .padding(.leading, 16)

                    Spacer()

                    // Screenshot button (bottom-right) - always visible
                    Button(action: {
                        takeScreenshot()
                    }) {
                        Image(systemName: "camera.fill")
                            .font(.system(size: 28))
                            .foregroundColor(.white)
                            .frame(width: 60, height: 60)
                            .background(Circle().fill(Color.blue).shadow(radius: 5))
                    }
                    .disabled(isLoading)
                    .padding(.trailing, 16)
                }
                .padding(.bottom, 20)
            }
            .zIndex(201)
        }
        .background(Color.black)
        .navigationTitle(navigationTitleWithDimensions)
        .navigationBarTitleDisplayMode(.inline)
        .toolbar {
            ToolbarItemGroup(placement: .navigationBarTrailing) {
                // Save button (only for new rooms, not when viewing from home)
                if allowSave {
                    Button(action: {
                        showRoomNameInput = true
                    }) {
                        Image(systemName: "square.and.arrow.down")
                    }
                    .disabled(isLoading)
                }

                // Recenter button
                Button(action: {
                    NotificationCenter.default.post(name: NSNotification.Name("RecenterWebGLCamera"), object: nil)
