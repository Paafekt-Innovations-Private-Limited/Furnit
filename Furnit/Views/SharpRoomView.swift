                    top: 0;
                    left: 0;
                    height: 4px;
                    background: #4a9eff;
                    width: 0%;
                    transition: width 0.3s;
                    z-index: 100;
                }
                #message {
                    position: absolute;
                    top: 50%;
                    left: 50%;
                    transform: translate(-50%, -50%);
                    color: white;
                    font-family: -apple-system, BlinkMacSystemFont, sans-serif;
                    font-size: 16px;
                    text-align: center;
                    z-index: 50;
                    padding: 20px;
                    background: rgba(0,0,0,0.7);
                    border-radius: 10px;
                }
                #spinner {
                    display: none;
                }
                #fps, #camid, #info {
                    display: none;
                }
            </style>
        </head>
        <body>
            <div id="progress"></div>
            <div id="message" style="display:none;"></div>
            <div id="spinner"></div>
            <div id="fps"></div>
            <div id="camid"></div>
            <div id="info"></div>
            <canvas id="canvas"></canvas>

            <script>
                // Log JS errors to console
                window.addEventListener('error', function(e) {
                    console.log('[JS Error] ' + e.message + ' at ' + e.filename + ':' + e.lineno);
                });

                // Room bounds from Swift (for camera positioning)
                const roomWidth = \(boundaryManager.width);
                const roomHeight = \(boundaryManager.height);
                const roomDepth = \(boundaryManager.depth);

                // Override URL params to point to our local PLY (pre-rotated classic version)
                const originalURLSearchParams = URLSearchParams;
                window.URLSearchParams = function(search) {
                    const params = new originalURLSearchParams(search);
                    const originalGet = params.get.bind(params);
                    params.get = function(key) {
                        if (key === 'url') {
                            return 'splat://local/room.ply';
                        }
                        return originalGet(key);
                    };
                    return params;
                };

                // Save default view matrix for recenter
                function saveDefaultViewMatrix() {
                    if (typeof viewMatrix !== 'undefined' && viewMatrix && viewMatrix.length === 16 && !window.defaultViewMatrix) {
                        window.defaultViewMatrix = viewMatrix.slice();
                    }
                }

                // LookAt camera helper
                function normalize(v) {
                    const len = Math.hypot(v[0], v[1], v[2]) || 1;
                    return [v[0]/len, v[1]/len, v[2]/len];
                }
                function cross(a, b) {
                    return [a[1]*b[2]-a[2]*b[1], a[2]*b[0]-a[0]*b[2], a[0]*b[1]-a[1]*b[0]];
                }
                function dot(a, b) {
                    return a[0]*b[0] + a[1]*b[1] + a[2]*b[2];
                }
                function lookAt(eye, target, up) {
                    const f = normalize([target[0]-eye[0], target[1]-eye[1], target[2]-eye[2]]);
                    const s = normalize(cross(f, up));
                    const u = cross(s, f);
                    return [
                        s[0], u[0], -f[0], 0,
                        s[1], u[1], -f[1], 0,
                        s[2], u[2], -f[2], 0,
                        -dot(s,eye), -dot(u,eye), dot(f,eye), 1
                    ];
                }

                // Camera adjustment disabled - let viewer handle it
                let cameraAdjusted = false;
                function adjustCameraDistance() {
                    if (cameraAdjusted) return;
                    if (typeof viewMatrix === 'undefined' || !viewMatrix) return;

                    // Just save the default view matrix for recenter, don't modify
                    defaultViewMatrix = viewMatrix.slice();
                    cameraAdjusted = true;
                }


                function hideLoadingMessage() {
                    const msg = document.getElementById('message');
                    if (msg) msg.style.display = 'none';
                }

                const jsStartTime = performance.now();

                window.addEventListener('load', function() {
                    // Keep default carousel animation (left-right orbit)

                    let checkCount = 0;
                    const checkLoaded = setInterval(function() {
                        checkCount++;

                        if (checkCount === 20) {
                            adjustCameraDistance();
                        }

                        if (checkCount === 30) {
                            saveDefaultViewMatrix();
                        }

                        if (checkCount === 50) {
                            hideLoadingMessage();
                            adjustCameraDistance();
                            saveDefaultViewMatrix();

                            const totalTime = performance.now() - jsStartTime;
                            if (window.webkit && window.webkit.messageHandlers && window.webkit.messageHandlers.splatLoaded) {
                                window.webkit.messageHandlers.splatLoaded.postMessage({ loaded: true, timeMs: totalTime });
                            }
                        }

                        if (checkCount > 60) {
                            clearInterval(checkLoaded);
                        }
                    }, 100);
                });
            </script>

            <!-- Load antimatter15/splat from local bundle -->
            <script src="splat://local/main.js"></script>
        </body>
        </html>
        """
    }

    class Coordinator: NSObject, WKNavigationDelegate, WKScriptMessageHandler {
        var webView: WKWebView?
        var schemeHandler: LocalFileSchemeHandler?
        let onLoaded: () -> Void
        private var hasNotifiedLoaded = false
        private var loadStartTime: CFAbsoluteTime = 0

        private var joystickTimer: Timer?

        init(onLoaded: @escaping () -> Void) {
            self.onLoaded = onLoaded
            self.loadStartTime = CFAbsoluteTimeGetCurrent()
            print("⏱️ [WebGL] Starting load...")
            super.init()

            // Listen for recenter notification
            NotificationCenter.default.addObserver(
                self,
                selector: #selector(recenterCamera),
                name: NSNotification.Name("RecenterWebGLCamera"),
                object: nil
            )

            // Listen for joystick movement
            NotificationCenter.default.addObserver(
                self,
                selector: #selector(handleJoystickMove(_:)),
                name: NSNotification.Name("WebGLJoystickMove"),
                object: nil
            )
        }

        deinit {
            joystickTimer?.invalidate()
            NotificationCenter.default.removeObserver(self)
        }

        @objc private func recenterCamera() {
            print("🎯 [WebGL] Recentering camera")
            let js = """
                if (typeof defaultViewMatrix !== 'undefined') {
                    viewMatrix = defaultViewMatrix.slice();
                    console.log('[Recenter] Reset to default view');
                }
                if (typeof carousel !== 'undefined') carousel = false;
            """
            webView?.evaluateJavaScript(js) { _, error in
                if let error = error {
                    print("❌ [WebGL] Recenter JS error: \(error)")
                }
            }
        }

        @objc private func handleJoystickMove(_ notification: Notification) {
            guard let userInfo = notification.userInfo,
                  let offset = userInfo["offset"] as? CGSize else { return }

            let moveSpeed: CGFloat = 0.012  // Reduced for smoother control
            let dx = Float(offset.width * moveSpeed)
            let dy = Float(-offset.height * moveSpeed)  // Invert Y for intuitive control

            // Skip if no movement
            if abs(dx) < 0.0005 && abs(dy) < 0.0005 { return }

            // JavaScript to move camera by modifying viewMatrix translation
            // viewMatrix is column-major: [12]=tx, [13]=ty, [14]=tz
            let js = """
                if (typeof viewMatrix !== 'undefined' && viewMatrix) {
                    // Stop carousel animation
                    if (typeof carousel !== 'undefined') carousel = false;

                    // Move camera (tx and tz for horizontal movement)
                    viewMatrix[12] += \(dx);  // Left/right
                    viewMatrix[14] += \(dy);  // Forward/backward
                }
            """
            webView?.evaluateJavaScript(js, completionHandler: nil)
        }

        func webView(_ webView: WKWebView, didFinish navigation: WKNavigation!) {
            let elapsed = (CFAbsoluteTimeGetCurrent() - loadStartTime) * 1000
            print("⏱️ [WebGL] HTML page loaded: \(String(format: "%.0f", elapsed))ms")

            // Fallback: notify loaded after delay if JS doesn't report
            DispatchQueue.main.asyncAfter(deadline: .now() + 5.0) {
                if !self.hasNotifiedLoaded {
                    self.hasNotifiedLoaded = true
                    let total = (CFAbsoluteTimeGetCurrent() - self.loadStartTime) * 1000
                    print("⏱️ [WebGL] Total load (fallback): \(String(format: "%.0f", total))ms")
                    self.onLoaded()
                }
            }
        }

        func webView(_ webView: WKWebView, didFail navigation: WKNavigation!, withError error: Error) {
            print("❌ [WebGL] Navigation failed: \(error)")
        }

        func webView(_ webView: WKWebView, didFailProvisionalNavigation navigation: WKNavigation!, withError error: Error) {
            print("❌ [WebGL] Provisional navigation failed: \(error)")
        }

        func userContentController(_ userContentController: WKUserContentController, didReceive message: WKScriptMessage) {
            if message.name == "splatLoaded" {
                if let body = message.body as? [String: Any], body["loaded"] != nil {
                    let elapsed = (CFAbsoluteTimeGetCurrent() - loadStartTime) * 1000
                    print("⏱️ [WebGL] Splat rendered: \(String(format: "%.0f", elapsed))ms total")

                    if !hasNotifiedLoaded {
                        hasNotifiedLoaded = true
                        DispatchQueue.main.async {
                            self.onLoaded()
                        }
                    }
                }
            }
        }
    }
}

// MARK: - WebGL Joystick Overlay

/// Joystick overlay specifically for WebGL camera control
/// Posts notifications that the WebView coordinator listens to
struct WebGLJoystickOverlay: View {
    @State private var offset: CGSize = .zero
    @State private var isDragging = false

    private let joystickSize: CGFloat = 120
    private let knobSize: CGFloat = 50
    private let maxOffset: CGFloat = 35

    var body: some View {
        VStack {
            Spacer()
            HStack {
                Spacer()
                // Joystick base
                ZStack {
                    // Outer ring
                    Circle()
                        .fill(Color.black.opacity(0.3))
                        .frame(width: joystickSize, height: joystickSize)

                    // Directional indicators
                    Circle()
                        .stroke(Color.white.opacity(0.3), lineWidth: 2)
                        .frame(width: joystickSize - 10, height: joystickSize - 10)

                    // Knob
                    Circle()
                        .fill(isDragging ? Color.blue : Color.white.opacity(0.8))
                        .frame(width: knobSize, height: knobSize)
                        .shadow(color: .black.opacity(0.3), radius: 4)
                        .offset(offset)
                }
                .gesture(
                    DragGesture()
                        .onChanged { value in
                            isDragging = true
                            let translation = value.translation

                            // Clamp to circular bounds
                            let distance = sqrt(translation.width * translation.width + translation.height * translation.height)
                            if distance > maxOffset {
                                let scale = maxOffset / distance
                                offset = CGSize(
                                    width: translation.width * scale,
                                    height: translation.height * scale
                                )
                            } else {
                                offset = translation
                            }

                            // Post notification for WebGL camera movement
                            NotificationCenter.default.post(
                                name: NSNotification.Name("WebGLJoystickMove"),
                                object: nil,
                                userInfo: ["offset": offset]
                            )
                        }
                        .onEnded { _ in
                            isDragging = false
                            withAnimation(.spring(response: 0.3, dampingFraction: 0.7)) {
                                offset = .zero
                            }
                            // Post zero offset to stop movement
                            NotificationCenter.default.post(
                                name: NSNotification.Name("WebGLJoystickMove"),
                                object: nil,
                                userInfo: ["offset": CGSize.zero]
                            )
                        }
                )
                Spacer()
            }
            .padding(.bottom, 40)
        }
    }
}

