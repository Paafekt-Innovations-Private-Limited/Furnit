/// Boundary manager for WebGL Gaussian splat rendering
/// Calculates optimal camera positions based on room dimensions
struct WebGLBoundaryManager {
    let width: Float
    let height: Float
    let depth: Float

    /// Default room dimensions when bounds not provided
    static let defaultBounds = WebGLBoundaryManager(width: 4.0, height: 3.0, depth: 2.0)

    init(width: Float, height: Float, depth: Float) {
        self.width = width
        self.height = height
        self.depth = depth
    }

    init(bounds: SIMD3<Float>?) {
        if let b = bounds {
            self.width = b.x
            self.height = b.y
            self.depth = b.z
        } else {
            self = Self.defaultBounds
        }
    }

    /// Calculate optimal camera position for viewing the room
    /// Camera positioned outside room, looking at room center
    /// Returns (eye position, target position) for lookAt camera
    func getOptimalCameraPosition() -> (eye: SIMD3<Float>, target: SIMD3<Float>) {
        // Classic PLY transform puts room at NEGATIVE Z (around -1 to -3)
        // Camera needs to be at POSITIVE Z looking toward negative Z
        // Room center is approximately at Z = -depth (negative)

        let roomCenterZ = -depth * 0.5  // Room is in negative Z space

        let eye = SIMD3<Float>(
            0,                          // Center X
            0.5,                        // Slightly above center
            roomCenterZ + depth * 2.0   // In front of room (more positive Z)
        )

        let target = SIMD3<Float>(
            0,                          // Center X
            0,                          // Look at floor level
            roomCenterZ                 // Room center in Z
        )

        return (eye: eye, target: target)
    }

    /// Get room center point
    var roomCenter: SIMD3<Float> {
        SIMD3<Float>(0, 0, 0)  // Classic PLY is centered at origin
    }

    /// Get room dimensions
    var dimensions: SIMD3<Float> {
        SIMD3<Float>(width, height, depth)
    }
}

/// Simple 3D Gaussian Splat viewer using WebGL (antimatter15/splat)
/// Renders PLY files generated by SHARP
struct SharpRoomView: View {
    let plyURL: URL
    let roomBounds: SIMD3<Float>?  // Room dimensions (width, height, depth) for camera positioning
    @Environment(\.dismiss) private var dismiss

    // Convenience initializer for backwards compatibility
    init(plyURL: URL, roomBounds: SIMD3<Float>? = nil) {
        self.plyURL = plyURL
        self.roomBounds = roomBounds
    }

    @State private var isLoading = true
    @State private var error: String?
    @State private var showingSmartyPants = false
    @State private var capturedImage: UIImage? = nil
    @State private var roomSnapshot: UIImage? = nil
    @State private var mlModel: MLModel? = nil

    /// Compute classic PLY URL (pre-rotated for antimatter15/splat)
    private var classicPlyURL: URL {
        let path = plyURL.path
        let classicPath = path.replacingOccurrences(of: ".ply", with: "_classic.ply")
        let classicURL = URL(fileURLWithPath: classicPath)
        if FileManager.default.fileExists(atPath: classicPath) {
            return classicURL
        }
        return plyURL
    }

    var body: some View {
        ZStack {
            // WebGL view using antimatter15/splat (with pre-rotated classic PLY)
            AntimatterSplatView(plyURL: classicPlyURL, roomBounds: roomBounds, onLoaded: {
                isLoading = false
            })
            .ignoresSafeArea()

            // Loading overlay
            if isLoading {
                VStack(spacing: 16) {
                    ProgressView()
                        .scaleEffect(1.5)
                        .tint(.white)

                    Text("Loading 3D room...")
                        .font(.subheadline)
                        .foregroundColor(.white)
                }
                .padding(24)
                .background(Color.black.opacity(0.7))
                .cornerRadius(12)
            }

            // Error overlay
            if let error = error {
                VStack(spacing: 16) {
                    Image(systemName: "exclamationmark.triangle")
                        .font(.largeTitle)
                        .foregroundColor(.orange)
                    Text(error)
                        .foregroundColor(.white)
                        .multilineTextAlignment(.center)
                }
                .padding(24)
                .background(Color.black.opacity(0.7))
                .cornerRadius(12)
            }

            // SmartyPants overlay (when active) - full screen
            if showingSmartyPants {
                SmartyPantsUIView(
                    capturedImage: $capturedImage,
                    roomImage: roomSnapshot,
                    mlModel: mlModel,
                    processInterval: 0.07,
                    active: true
                )
                .ignoresSafeArea()
                .zIndex(100)
            }

            // HUD (hide when SmartyPants active)
            if !showingSmartyPants {
                VStack {
                    HStack {
                        Spacer()

                        // Recenter button
                        Button(action: {
                            NotificationCenter.default.post(name: NSNotification.Name("RecenterWebGLCamera"), object: nil)
                        }) {
                            Image(systemName: "viewfinder")
                                .font(.system(size: 20))
                                .foregroundColor(.white)
                                .frame(width: 44, height: 44)
                                .background(Circle().fill(Color.gray.opacity(0.8)))
                        }
                        .disabled(isLoading)
                    }
                    .padding()
                    Spacer()
                }
            }

            // Bottom buttons (brain left, screenshot right when SmartyPants active)
            VStack {
                Spacer()
                HStack {
                    // Brain button (bottom-left)
                    Button(action: {
                        if showingSmartyPants {
                            showingSmartyPants = false
                        } else {
                            // Request Metal view snapshot
                            NotificationCenter.default.post(name: NSNotification.Name("CaptureMetalSnapshot"), object: nil)
                            // Wait for snapshot then show SmartyPants
                            DispatchQueue.main.asyncAfter(deadline: .now() + 0.3) {
                                showingSmartyPants = true
                            }
                        }
                    }) {
                        Image(systemName: "brain.head.profile")
                            .font(.system(size: 28))
                            .foregroundColor(.white)
                            .frame(width: 60, height: 60)
                            .background(Circle().fill(showingSmartyPants ? Color.green : Color.blue).shadow(radius: 5))
                    }
                    .disabled(isLoading)
                    .padding(.leading, 16)

                    Spacer()

                    // Screenshot button (bottom-right) - only when SmartyPants active
                    if showingSmartyPants {
                        Button(action: {
                            takeScreenshot()
                        }) {
                            Image(systemName: "square.and.arrow.up")
                                .font(.system(size: 28))
                                .foregroundColor(.white)
                                .frame(width: 60, height: 60)
                                .background(Circle().fill(Color.blue).shadow(radius: 5))
                        }
                        .padding(.trailing, 16)
                    }
                }
                .padding(.bottom, 20)
            }
            .zIndex(201)
        }
        .background(Color.black)
        .navigationTitle("3D Room")
        .navigationBarTitleDisplayMode(.inline)
        .onAppear {
            loadMLModel()
        }
    }

    // MARK: - Screenshot

    private func takeScreenshot() {
        logDebug("📸 Taking screenshot...")

        // Capture the window
        let scenes = UIApplication.shared.connectedScenes.compactMap { $0 as? UIWindowScene }
        let windows = scenes.flatMap { $0.windows }
        guard let window = windows.first(where: { $0.isKeyWindow }) ?? windows.first else {
            logDebug("❌ No window found")
            return
        }

        let format = UIGraphicsImageRendererFormat()
        format.scale = UIScreen.main.scale
        let renderer = UIGraphicsImageRenderer(bounds: window.bounds, format: format)
        let image = renderer.image { _ in
            window.drawHierarchy(in: window.bounds, afterScreenUpdates: true)
        }

        logDebug("📸 Screenshot captured, saving to Photos...")

        // Save to photos
        UIImageWriteToSavedPhotosAlbum(image, nil, nil, nil)
        logDebug("✅ Screenshot saved to Photos")
    }

    // MARK: - Load ML Model for SmartyPants

    private func loadMLModel() {
        guard mlModel == nil else { return }

        let candidateNames = [
            ("yoloe-11l-seg-pf", "mlmodelc"),
        ]

        for (name, ext) in candidateNames {
            if let url = Bundle.main.url(forResource: name, withExtension: ext) {
                do {
                    let config = MLModelConfiguration()
                    config.computeUnits = .cpuOnly
                    let model = try MLModel(contentsOf: url, configuration: config)
                    self.mlModel = model
                    logDebug("✅ [SharpRoomView] Loaded MLModel '\(name).\(ext)'")
                    break
                } catch {
                    logDebug("❌ [SharpRoomView] Failed to load \(name).\(ext): \(error)")
                }
            }
        }
    }

}

