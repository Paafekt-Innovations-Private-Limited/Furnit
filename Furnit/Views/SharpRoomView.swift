// MARK: - WebGL Boundary Manager

/// Boundary manager for WebGL Gaussian splat rendering
/// Calculates optimal camera positions based on room dimensions
struct WebGLBoundaryManager {
    let width: Float
    let height: Float
    let depth: Float

    /// Default room dimensions when bounds not provided
    static let defaultBounds = WebGLBoundaryManager(width: 4.0, height: 3.0, depth: 2.0)

    init(width: Float, height: Float, depth: Float) {
        self.width = width
        self.height = height
        self.depth = depth
    }

    init(bounds: SIMD3<Float>?) {
        if let b = bounds {
            self.width = b.x
            self.height = b.y
            self.depth = b.z
        } else {
            self = Self.defaultBounds
        }
    }

    /// Calculate optimal camera position for viewing the room
    /// Camera positioned outside room, looking at room center
    /// Returns (eye position, target position) for lookAt camera
    func getOptimalCameraPosition() -> (eye: SIMD3<Float>, target: SIMD3<Float>) {
        // Classic PLY transform: (x, -y, -z)
        // Room ends up entirely in NEGATIVE Z (front wall around -1, back wall around -depth-1)
        // Room is roughly centered in X and Y around origin
        // Estimate room center Z based on typical SHARP output pattern

        let roomCenterZ = -(depth / 2 + 1.5)  // Room center (estimated from logs)

        let eye = SIMD3<Float>(
            0,                          // Center X
            0,                          // Center Y (room is symmetric)
            6.0                         // Further back (positive Z, outside room)
        )

        let target = SIMD3<Float>(
            0,                          // Center X
            0,                          // Center Y
            roomCenterZ                 // Room center Z
        )

        return (eye: eye, target: target)
    }

    /// Get room center point
    var roomCenter: SIMD3<Float> {
        SIMD3<Float>(0, 0, 0)  // Classic PLY is centered at origin
    }

    /// Get room dimensions
    var dimensions: SIMD3<Float> {
        SIMD3<Float>(width, height, depth)
    }
}

/// Simple 3D Gaussian Splat viewer using WebGL (antimatter15/splat)
/// Renders PLY files generated by SHARP
struct SharpRoomView: View {
    let plyURL: URL
    let roomMeasurements: RoomMeasurements?  // Room measurements for display
    let allowSave: Bool  // Show save button (true for new rooms, false for viewing from home)
    @Environment(\.dismiss) private var dismiss

    // Convenience initializer for backwards compatibility
    init(plyURL: URL, roomMeasurements: RoomMeasurements? = nil, allowSave: Bool = true) {
        self.plyURL = plyURL
        self.roomMeasurements = roomMeasurements
        self.allowSave = allowSave
    }

    @State private var isLoading = true
    @State private var error: String?
    @State private var showingSmartyPants = false
    @State private var capturedImage: UIImage? = nil
    @State private var roomSnapshot: UIImage? = nil
    @State private var mlModel: MLModel? = nil

    // Save room state
    @StateObject private var modelManager = USDZModelManager()
    @State private var isSavingRoom = false
    @State private var saveProgress: Double = 0.0
    @State private var savingTimer: Timer?
    @State private var showSaveAlert = false
    @State private var saveAlertMessage = ""
    @State private var saveWasSuccessful = false
    @State private var showRoomNameInput = false
    @State private var roomName = ""
    @State private var showShareSheet = false

    /// Compute classic PLY URL (pre-rotated for antimatter15/splat)
    private var classicPlyURL: URL {
        let path = plyURL.path
        let classicPath = path.replacingOccurrences(of: ".ply", with: "_classic.ply")
        let classicURL = URL(fileURLWithPath: classicPath)
        if FileManager.default.fileExists(atPath: classicPath) {
            return classicURL
        }
        return plyURL
    }

    /// Compute 3DGS PLY URL (SuperSplat compatible format)
    private var threeDGSPlyURL: URL {
        let path = plyURL.path
        let threeDGSPath = path.replacingOccurrences(of: ".ply", with: "_3dgs.ply")
        let threeDGSURL = URL(fileURLWithPath: threeDGSPath)
        if FileManager.default.fileExists(atPath: threeDGSPath) {
            return threeDGSURL
        }
        return plyURL
    }

    var body: some View {
        ZStack {
            // WebGL view using SparkJS (with original PLY)
            AntimatterSplatView(plyURL: plyURL, roomBounds: roomMeasurements?.boundingBox, onLoaded: {
                isLoading = false
            })
            .ignoresSafeArea()

            // Loading overlay
            if isLoading {
                VStack(spacing: 16) {
                    ProgressView()
                        .scaleEffect(1.5)
                        .tint(.white)

                    Text("Loading 3D room...")
                        .font(.subheadline)
                        .foregroundColor(.white)
                }
                .padding(24)
                .background(Color.black.opacity(0.7))
                .cornerRadius(12)
            }

            // Error overlay
            if let error = error {
                VStack(spacing: 16) {
                    Image(systemName: "exclamationmark.triangle")
                        .font(.largeTitle)
                        .foregroundColor(.orange)
                    Text(error)
                        .foregroundColor(.white)
                        .multilineTextAlignment(.center)
                }
                .padding(24)
                .background(Color.black.opacity(0.7))
                .cornerRadius(12)
            }

            // SmartyPants overlay (when active) - full screen
            if showingSmartyPants {
                SmartyPantsUIView(
                    capturedImage: $capturedImage,
                    roomImage: roomSnapshot,
                    mlModel: mlModel,
                    processInterval: 0.07,
                    active: true
                )
                .ignoresSafeArea()
                .zIndex(100)
            }


            // Save progress overlay
            if isSavingRoom {
                saveRoomProgressOverlay
            }

            // WebGL Joystick overlay (hide when SmartyPants active)
            if !showingSmartyPants {
                WebGLJoystickOverlay()
                    .zIndex(99997)
            }

            // Bottom buttons (brain left, screenshot right)
            VStack {
                Spacer()
                HStack {
                    // Brain button (bottom-left)
                    Button(action: {
                        if showingSmartyPants {
                            showingSmartyPants = false
                        } else {
                            // Request Metal view snapshot
                            NotificationCenter.default.post(name: NSNotification.Name("CaptureMetalSnapshot"), object: nil)
                            // Wait for snapshot then show SmartyPants
                            DispatchQueue.main.asyncAfter(deadline: .now() + 0.3) {
                                showingSmartyPants = true
                            }
                        }
                    }) {
                        Image(systemName: "brain.head.profile")
                            .font(.system(size: 28))
                            .foregroundColor(.white)
                            .frame(width: 60, height: 60)
                            .background(Circle().fill(showingSmartyPants ? Color.green : Color.blue).shadow(radius: 5))
                    }
                    .disabled(isLoading)
                    .padding(.leading, 16)

                    Spacer()

                    // Screenshot button (bottom-right) - always visible
                    Button(action: {
                        takeScreenshot()
                    }) {
                        Image(systemName: "camera.fill")
                            .font(.system(size: 28))
                            .foregroundColor(.white)
                            .frame(width: 60, height: 60)
                            .background(Circle().fill(Color.blue).shadow(radius: 5))
                    }
                    .disabled(isLoading)
                    .padding(.trailing, 16)
                }
                .padding(.bottom, 20)
            }
            .zIndex(201)
        }
        .background(Color.black)
        .navigationTitle(navigationTitleWithDimensions)
        .navigationBarTitleDisplayMode(.inline)
        .toolbar {
            ToolbarItemGroup(placement: .navigationBarTrailing) {
                // Share button - export PLY to Files/AirDrop
                Button(action: {
                    showShareSheet = true
                }) {
                    Image(systemName: "square.and.arrow.up")
                }
                .disabled(isLoading)

                // Save button (only for new rooms, not when viewing from home)
                if allowSave {
                    Button(action: {
                        showRoomNameInput = true
                    }) {
                        Image(systemName: "square.and.arrow.down")
                    }
                    .disabled(isLoading)
                }

                // Recenter button
                Button(action: {
                    NotificationCenter.default.post(name: NSNotification.Name("RecenterWebGLCamera"), object: nil)
                }) {
                    Image(systemName: "viewfinder")
                }
                .disabled(isLoading)
            }
        }
        .sheet(isPresented: $showShareSheet) {
            // Share the 3DGS format (SuperSplat compatible)
            ShareSheet(activityItems: [threeDGSPlyURL])
        }
        .onAppear {
            loadMLModel()
        }
        // Room name input alert
        .alert("Save Room", isPresented: $showRoomNameInput) {
            TextField("Room name", text: $roomName)
            Button("Cancel", role: .cancel) {
                roomName = ""
            }
            Button("Save") {
                startSavingRoom()
            }
            .disabled(roomName.isEmpty)
        } message: {
            Text("Enter a name for this room")
        }
        // Save result alert
        .alert("Room Save", isPresented: $showSaveAlert) {
            Button("OK", role: .cancel) {
                if saveWasSuccessful {
                    // Dismiss entire sheet and go back to home view
                    NotificationCenter.default.post(name: NSNotification.Name("DismissPhotoRoomSheet"), object: nil)
                }
            }
        } message: {
            Text(saveAlertMessage)
        }
    }

    // MARK: - Screenshot

    private func takeScreenshot() {
        logDebug("📸 Taking screenshot...")

        // Capture the window
        let scenes = UIApplication.shared.connectedScenes.compactMap { $0 as? UIWindowScene }
        let windows = scenes.flatMap { $0.windows }
        guard let window = windows.first(where: { $0.isKeyWindow }) ?? windows.first else {
            logDebug("❌ No window found")
            return
        }

        let format = UIGraphicsImageRendererFormat()
        format.scale = UIScreen.main.scale
        let renderer = UIGraphicsImageRenderer(bounds: window.bounds, format: format)
        let image = renderer.image { _ in
            window.drawHierarchy(in: window.bounds, afterScreenUpdates: true)
        }

        logDebug("📸 Screenshot captured, saving to Photos...")

        // Save to photos
        UIImageWriteToSavedPhotosAlbum(image, nil, nil, nil)
        logDebug("✅ Screenshot saved to Photos")
    }

    // MARK: - Load ML Model for SmartyPants

    private func loadMLModel() {
        guard mlModel == nil else { return }

        let candidateNames = [
            ("yoloe-11l-seg-pf", "mlmodelc"),
        ]

        for (name, ext) in candidateNames {
            if let url = Bundle.main.url(forResource: name, withExtension: ext) {
                do {
                    let config = MLModelConfiguration()
                    config.computeUnits = .cpuOnly
                    let model = try MLModel(contentsOf: url, configuration: config)
                    self.mlModel = model
                    logDebug("✅ [SharpRoomView] Loaded MLModel '\(name).\(ext)'")
                    break
                } catch {
                    logDebug("❌ [SharpRoomView] Failed to load \(name).\(ext): \(error)")
                }
            }
        }
    }

    // MARK: - Navigation Title with Dimensions
    private var navigationTitleWithDimensions: String {
        // Use actual room measurements if available
        let wallWidth = roomMeasurements?.frontWallWidth ?? 4.0
        let wallHeight = roomMeasurements?.frontWallHeight ?? 3.0
        let depth = roomMeasurements?.roomDepth ?? 2.0

        return String(format: "%.1f × %.1f × %.1f m", wallWidth, wallHeight, depth)
    }

    /// Get file size of the PLY file
    private func getFileSize() -> String {
        let url = classicPlyURL
        guard let attributes = try? FileManager.default.attributesOfItem(atPath: url.path),
              let size = attributes[.size] as? UInt64 else {
            return "Unknown"
        }

        let mb = Double(size) / (1024 * 1024)
        if mb >= 1.0 {
            return String(format: "%.1f MB", mb)
        } else {
            let kb = Double(size) / 1024
            return String(format: "%.0f KB", kb)
        }
    }

    // MARK: - Save Room Progress Overlay
    private var saveRoomProgressOverlay: some View {
        ZStack {
            Color.black.opacity(0.9)
                .ignoresSafeArea()

            VStack(spacing: 24) {
                ZStack {
                    Circle()
                        .fill(Color.green.opacity(0.2))
                        .frame(width: 100, height: 100)

                    Image(systemName: "square.and.arrow.down")
                        .font(.system(size: 40))
                        .foregroundColor(.green)
                }

                Text("Saving Room...")
                    .font(.title2)
                    .fontWeight(.semibold)
                    .foregroundColor(.white)

                ProgressView(value: saveProgress)
                    .progressViewStyle(LinearProgressViewStyle(tint: .green))
                    .frame(width: 200)

                Text("\(Int(saveProgress * 100))%")
                    .font(.headline)
                    .foregroundColor(.gray)

                Button("Cancel") {
                    cancelSavingRoom()
                }
                .foregroundColor(.red)
                .padding(.top, 20)
            }
        }
    }

    // MARK: - Save Room Functions
    private func startSavingRoom() {
        guard !roomName.isEmpty else { return }

        let savedName = roomName
        logDebug("💾 [SharpRoomView] Starting room save: \(savedName)")

        withAnimation(.easeIn(duration: 0.3)) {
            isSavingRoom = true
            saveProgress = 0.0
        }

        var saveStarted = false
        var saveCompleted = false
        var saveSuccess = false
        var saveError: String?

        savingTimer = Timer.scheduledTimer(withTimeInterval: 0.05, repeats: true) { timer in
            if !saveStarted || (saveStarted && saveCompleted) {
                self.saveProgress += 0.015
            }

            if self.saveProgress >= 0.6 && !saveStarted {
                saveStarted = true
                // Save the classic PLY file (pre-transformed for correct viewing)
                self.modelManager.savePLY(from: self.classicPlyURL, name: savedName) { success, error in
                    DispatchQueue.main.async {
                        saveCompleted = true
                        saveSuccess = success
                        saveError = error
                        logDebug(success ? "✅ [SharpRoomView] Room saved" : "❌ [SharpRoomView] Save failed: \(error ?? "unknown")")
                    }
                }
            }

            if self.saveProgress >= 1.0 && saveCompleted {
                timer.invalidate()
                self.savingTimer = nil

                withAnimation(.easeOut(duration: 0.3)) {
                    self.isSavingRoom = false
                }

                DispatchQueue.main.asyncAfter(deadline: .now() + 0.3) {
                    if saveSuccess {
                        self.saveAlertMessage = "Room '\(savedName)' saved successfully!"
                        self.saveWasSuccessful = true
                    } else {
                        self.saveAlertMessage = "Failed to save: \(saveError ?? "Unknown error")"
                        self.saveWasSuccessful = false
                    }
                    self.showSaveAlert = true
                    self.roomName = ""
                }
            }
        }
    }

    private func cancelSavingRoom() {
        savingTimer?.invalidate()
        savingTimer = nil

        withAnimation(.easeOut(duration: 0.2)) {
            isSavingRoom = false
            saveProgress = 0.0
        }

        roomName = ""
        logDebug("❌ [SharpRoomView] Room save cancelled")
    }

}

